package ufpb.br.aps;

public class ClientePF extends Cliente {

	private String nome;
	private String cpf;
	
	public ClientePF(String nome, String cpf) {
		
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getCpf() {
		return cpf;
	}
	public void setCpf(String cpf) {
		this.cpf = cpf;
	}
}
